import sortByHealth from '../matchers.js';

test('sortByHealth 10 100 80', () => {
  expect(sortByHealth(
    [
      { name: 'мечник', health: 10 },
      { name: 'маг', health: 100 },
      { name: 'лучник', health: 80 },
    ],
  )).toEqual(
    [
      { name: 'маг', health: 100 },
      { name: 'лучник', health: 80 },
      { name: 'мечник', health: 10 },
    ],
  );
});
